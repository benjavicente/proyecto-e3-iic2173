import React, { useState } from 'react'
import { useUser } from '@auth0/nextjs-auth0'
import Head from 'next/head'

import Navbar from '../../components/Navbar'
import Footer from '../../components/Footer'
import UserInfo from '../../components/UserInfo'

import { getApi } from '../../lib/api'

import styles from '../../styles/Home.module.css'

function UsersPage() {
  const { user } = useUser();  

  const [loading, setLoading] = useState(true);
  const [usersData, setUsersData] = useState(null);

  const [page, setPage] = useState(1);

  const prevPage = () => {
    setPage(page-1);
    setLoading(true);
  }
  const nextPage = () => {
    setPage(page+1);
    setLoading(true);
  }

  if (loading) {
    getApi('api/users', {'page': page}) 
      .then(data => {
        setUsersData(JSON.parse(data));
        setLoading(false);
      });

    return (
      <h2>Cargando</h2>
    )
  }

  const Users = usersData.map((user) => {
    return (
      <UserInfo user={user} key={user.id} />     
    )
  });
  
  return (
    <div>
      <Head>
        <title>PingToc</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
    
      <Navbar logged={user !== undefined}/>

      <div className={styles.centerContainer}>
        <h1> Lista de usuarios </h1>
        <p> P치gina {page} </p>
        <button className={styles.button} onClick={prevPage} disabled={page == 1}>P치gina anterior</button>
        <button className={styles.button} onClick={nextPage} disabled={usersData.length == 0}>Siguiente p치gina</button>
      </div> 

      <div className={styles.flexWrapContainer}>
        {usersData.length !== 0 ? Users : <h2> No hay m치s usuarios</h2>}
      </div>

      <Footer />
    </div>
  )
}

export default UsersPage
